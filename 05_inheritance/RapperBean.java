public class RapperBean {
	private int cars;
	private int songs;
	private String realName;
	
	public int getCars() {
		return cars;
	}
	public void setCars(int pCars) {
		cars = pCars;
	}
	public int getSongs() {
		return songs;
	}
	public void setSongs(int pSongs) {
		songs = pSongs;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String pRealName) {
		realName = pRealName;
	}   
}
